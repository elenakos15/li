// 4. Работа с объектом сотрудников и их зарплатами.
const obj = {
    "Коля": '200',
    "Вася": '300',
    "Петя": '400'
};

for (let name in obj) {
    console.log(`${name} — зарплата ${obj[name]} долларов.`);
}

// 5. Делить число 1000 на 2 до тех пор, пока оно не станет меньше 50.
let n = 1000;
let num = 0; // количество итераций

while (n >= 50) {
    n = n / 2;
    num++;
}

console.log(`Результат: ${n}, Количество итераций: ${num}`);

// 6. Программа для определения пятницы в месяце.
const firstFriday = 5; // номер первой пятницы (например, 5)
const daysInMonth = 31;

for (let i = firstFriday; i <= daysInMonth; i += 7) {
    console.log(`Сегодня пятница, ${i}-е число. Необходимо подготовить отчет.`);
}

